function clickMenu(){
    if(menu.style.display == "block"){
        menu.style.display = "none"
        menu.style.color = "#000"
        menu.style.left === "80px"
        menu.style.left = "0";
    }
    else{
        menu.style.display = "block"
        menu.style.color = "#000"
        menu.style.left = "80px";
    }
}